import Head from "next/head";
import YouTube from "react-youtube";
import { Options } from "youtube-player/dist/types";
import styles from "@/styles/home.module.css";
import { ReactNode, useEffect, useState } from "react";
import { youtube_v3 } from "@googleapis/youtube";
import Linkify from "linkify-react";
import "linkify-plugin-hashtag";
import "linkify-plugin-mention";
import Link from "next/link";
import { IntermediateRepresentation } from "linkifyjs";
import { getFormattedDateTime } from "@/utils/date-utils";
import Image from "next/image";

export default function Home() {
  const videoId: string = "Z2Z9V-4DMGw";
  const opts: Options = {
    playerVars: {
      rel: 0,
    },
  };

  const [data, setData] = useState<youtube_v3.Schema$SearchResult[]>([]);

  useEffect(() => {
    const f = async () => {
      const data = await fetch("/api/youtube/search");
      const json = (await data.json()) as youtube_v3.Schema$SearchListResponse;
      const list = json.items!;
      setData(list);
    };
    f();
  }, []);

  const renderLink = ({ attributes, content }: IntermediateRepresentation) => {
    const { href, ...props } = attributes;

    return (
      <Link href={href} {...props}>
        {content}
      </Link>
    );
  };

  return (
    <>
      <Head>
        <title>Youtube API Sample</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        <h1>Youtube</h1>

        {data.length > 0 && (
          <>
            <h2>検索結果</h2>
            {data.map((item) => (
              <div key={item.id?.videoId} className={styles.row}>
                {/* eslint-disable @next/next/no-img-element */}
                <img
                  className={styles.thumbnail}
                  src={item.snippet?.thumbnails?.high?.url!}
                  alt={item.snippet?.title!}
                />
                {/* eslint-enable @next/next/no-img-element */}
                <div className={styles.contents}>
                  <p>{item.id?.videoId}</p>
                  <p>{item.snippet?.title}</p>
                </div>
              </div>
            ))}
          </>
        )}
      </main>
    </>
  );
}
